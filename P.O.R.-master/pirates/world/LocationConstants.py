def getParentIsland(locationId):
    return LocationIds.Location2Island.get(locationId)


def getLocationList(locationId):
    return LocationIds.LocationDefs.get(locationId)


def isInArea(location, area):
    if location == LocationIds.ANY_LOCATION:
        return (True, '')
    
    if area is None:
        return (False, '')
    
    locationList = getLocationList(location)
    if locationList:
        if area in locationList:
            return (True, '')
        
    else:
        warning = 'No location list for: %s' % location
        if location == area:
            return (True, warning)
        
        return (False, warning)
    return (False, '')

